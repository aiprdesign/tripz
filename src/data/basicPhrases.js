/** Default when country has no phrase list: English only + note to learn locally */
function defaultPhrases() {
  return PHRASES.map((p) => ({ en: p.en, local: '(learn locally)', pron: '—' }))
}

/**
 * Get basic tourist phrases for a country.
 * Returns array of { en, local, pron } in same order as PHRASES.
 */
export function getBasicPhrases(countryName) {
  const list = BY_COUNTRY[countryName]
  if (!list || list.length === 0) return defaultPhrases()
  return PHRASES.map((p, i) => ({
    en: p.en,
    local: list[i]?.local ?? '(—)',
    pron: list[i]?.pron ?? '—',
  }))
}

export { PHRASES, BY_COUNTRY }
