/**
 * Get trip-planning info for a city and country (costs, currency, things to avoid).
 * Uses city override when available, then country info, then defaults.
 * @param {string} city
 * @param {string} country
 * @returns {{ currency: string, currencyCode: string, costOfLivingLevel: string, hotelAvgPerNight: string, foodAvgPerDay: string, thingsToAvoid: string[] }}
 */
export function getCityTripInfo(city, country) {
  const countryInfo = COUNTRY_INFO[country] || DEFAULT_INFO
  const cityKey = `${city}|${country}`
  const cityOverride = CITY_OVERRIDES[cityKey]
  const base = { ...DEFAULT_INFO, ...countryInfo }
  const merged = cityOverride ? { ...base, ...cityOverride } : base
  return {
    currency: merged.currency ?? base.currency,
    currencyCode: merged.currencyCode ?? base.currencyCode,
    costOfLivingLevel: merged.costOfLivingLevel ?? base.costOfLivingLevel,
    hotelAvgPerNight: merged.hotelAvgPerNight ?? base.hotelAvgPerNight,
    foodAvgPerDay: merged.foodAvgPerDay ?? base.foodAvgPerDay,
    thingsToAvoid: Array.isArray(merged.thingsToAvoid) ? merged.thingsToAvoid : base.thingsToAvoid,
  }
}
